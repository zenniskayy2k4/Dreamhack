unsigned int rol(unsigned int x, unsigned int n){
    return ((x << n) | (x >> (8 - n))) & 0xFF;
}
unsigned int ror(unsigned int x, unsigned int n){
    return ((x >> n) | (x << (8 - n))) & 0xFF;
}

